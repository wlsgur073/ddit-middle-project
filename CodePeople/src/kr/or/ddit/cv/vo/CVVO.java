package kr.or.ddit.cv.vo;

public class CVVO {
	private Integer cvNo;
	private Integer hrNo;
	private Integer cvOpen;
	private Integer cvSal;
	private String cvCmt;
	private Integer cvCnt;
	private Integer atchFileId;
	public Integer getCvNo() {
		return cvNo;
	}
	public void setCvNo(Integer cvNo) {
		this.cvNo = cvNo;
	}
	public Integer getHrNo() {
		return hrNo;
	}
	public void setHrNo(Integer hrNo) {
		this.hrNo = hrNo;
	}
	public Integer getCvOpen() {
		return cvOpen;
	}
	public void setCvOpen(Integer cvOpen) {
		this.cvOpen = cvOpen;
	}
	public Integer getCvSal() {
		return cvSal;
	}
	public void setCvSal(Integer cvSal) {
		this.cvSal = cvSal;
	}
	public String getCvCmt() {
		return cvCmt;
	}
	public void setCvCmt(String cvCmt) {
		this.cvCmt = cvCmt;
	}
	public Integer getCvCnt() {
		return cvCnt;
	}
	public void setCvCnt(Integer cvCnt) {
		this.cvCnt = cvCnt;
	}
	public Integer getAtchFileId() {
		return atchFileId;
	}
	public void setAtchFileId(Integer atchFileId) {
		this.atchFileId = atchFileId;
	}
}
