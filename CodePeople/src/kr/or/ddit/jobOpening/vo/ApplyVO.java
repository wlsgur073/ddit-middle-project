package kr.or.ddit.jobOpening.vo;

public class ApplyVO {

	private Integer applNo;
	private Integer hrNo;
	private Integer joNo;
	private Integer jtNo;
	public Integer getApplNo() {
		return applNo;
	}
	public void setApplNo(Integer applNo) {
		this.applNo = applNo;
	}
	public Integer getHrNo() {
		return hrNo;
	}
	public void setHrNo(Integer hrNo) {
		this.hrNo = hrNo;
	}
	public Integer getJoNo() {
		return joNo;
	}
	public void setJoNo(Integer joNo) {
		this.joNo = joNo;
	}
	public Integer getJtNo() {
		return jtNo;
	}
	public void setJtNo(Integer jtNo) {
		this.jtNo = jtNo;
	}
}
